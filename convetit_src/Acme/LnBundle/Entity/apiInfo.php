<?php
namespace Acme\LnBundle\Entity;

Class apiInfo
{
	
    protected $id;
    
    protected $api_name;
    
    protected $display_name;

    protected $original_url;

    protected $api_key;
    
    protected $api_secret_key;
    
    protected $api_username;
    
    protected $api_status=1;
    /**
     * @var string
     */
    private $api_secrete_key;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set api_name
     *
     * @param string $apiName
     * @return apiInfo
     */
    public function setApiName($apiName)
    {
        $this->api_name = $apiName;
    
        return $this;
    }

    /**
     * Get api_name
     *
     * @return string 
     */
    public function getApiName()
    {
        return $this->api_name;
    }

    /**
     * Set display_name
     *
     * @param string $displayName
     * @return apiInfo
     */
    public function setDisplayName($displayName)
    {
        $this->display_name = $displayName;
    
        return $this;
    }

    /**
     * Get display_name
     *
     * @return string 
     */
    public function getDisplayName()
    {
        return $this->display_name;
    }

    /**
     * Set original_url
     *
     * @param string $originalUrl
     * @return apiInfo
     */
    public function setOriginalUrl($originalUrl)
    {
        $this->original_url = $originalUrl;
    
        return $this;
    }

    /**
     * Get original_url
     *
     * @return string 
     */
    public function getOriginalUrl()
    {
        return $this->original_url;
    }

    /**
     * Set api_key
     *
     * @param string $apiKey
     * @return apiInfo
     */
    public function setApiKey($apiKey)
    {
        $this->api_key = $apiKey;
    
        return $this;
    }

    /**
     * Get api_key
     *
     * @return string 
     */
    public function getApiKey()
    {
        return $this->api_key;
    }

    /**
     * Set api_secrete_key
     *
     * @param string $apiSecreteKey
     * @return apiInfo
     */
    public function setApiSecreteKey($apiSecreteKey)
    {
        $this->api_secrete_key = $apiSecreteKey;
    
        return $this;
    }

    /**
     * Get api_secrete_key
     *
     * @return string 
     */
    public function getApiSecreteKey()
    {
        return $this->api_secrete_key;
    }

    /**
     * Set api_status
     *
     * @param integer $apiStatus
     * @return apiInfo
     */
    public function setApiStatus($apiStatus)
    {
        $this->api_status = $apiStatus;
    
        return $this;
    }

    /**
     * Get api_status
     *
     * @return integer 
     */
    public function getApiStatus()
    {
        return $this->api_status;
    }
}